let currentLoc: Localization = EnglishLoc; // Default language is English

export function switchLanguage(
  lang: "English" | "Emoji" | "Spanish" | "Cantonese" | "Arabic",
) {
  if (lang === "Emoji") {
    currentLoc = EmojiLoc;
  } else if (lang === "Spanish") {
    currentLoc = SpanishLoc;
  } else if (lang === "Cantonese") {
    currentLoc = CantoneseLoc;
  } else if (lang === "Arabic") {
    currentLoc = ArabicLoc;
  } else {
    currentLoc = EnglishLoc; // Default to English
  }
}

export function getCurrentLocalization(): Localization {
  return currentLoc;
}
